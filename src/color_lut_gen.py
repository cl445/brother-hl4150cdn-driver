# ---------------------------------------------------------------------------
# 1D Basis evaluation helpers
# ---------------------------------------------------------------------------
def _eval_1d_basis(val: npt.NDArray[np.float64], n_grid: int) -> npt.NDArray[np.float64]:
    """Evaluate 1D hat-function basis for each grid point.

    Args:
        val: Input values normalized to [0, n_grid-1] range. Shape (N,).
        n_grid: Number of grid points.

    Returns:
        Basis matrix of shape (N, n_grid).
    """
    n = len(val)
    basis = np.zeros((n, n_grid), dtype=np.float64)
    lo = np.floor(val).astype(np.int32)
    lo = np.clip(lo, 0, n_grid - 2)
    frac = val - lo

    for k in range(n_grid):
        mask_lo = lo == k
        mask_hi = (lo + 1) == k
        if k < n_grid - 1:
            basis[mask_lo, k] += 1.0 - frac[mask_lo]
        if k > 0:
            basis[mask_hi, k] += frac[mask_hi]

    return basis


# ---------------------------------------------------------------------------
# LUT generation
# ---------------------------------------------------------------------------
def generate_rgb_default_lut() -> npt.NDArray[np.int32]:
    """Generate the 17x17x17 CMYK color LUT from parametric coefficients.

    Returns:
        (4913, 4) int32 array with columns [C, M, Y, K], values 0-255.
    """
    n = _LUT_DIM**3

    # Build RGB grid coordinates
    grid = np.zeros((n, 3), dtype=np.float64)
    idx = np.arange(n)
    grid[:, 0] = (idx // (_LUT_DIM * _LUT_DIM)) * (255.0 / (_LUT_DIM - 1))
    grid[:, 1] = ((idx // _LUT_DIM) % _LUT_DIM) * (255.0 / (_LUT_DIM - 1))
    grid[:, 2] = (idx % _LUT_DIM) * (255.0 / (_LUT_DIM - 1))
    grid = np.round(grid)

    result = np.zeros((n, 4), dtype=np.float64)

    for ch in range(4):
        value = np.zeros(n, dtype=np.float64)

        # Layer 1: 1D tone curves
        for rgb_ch in range(3):
            t = grid[:, rgb_ch] / (255.0 / (_LUT_DIM - 1))
            basis = _eval_1d_basis(t, _LUT_DIM)
            value += basis @ _TONE_CURVES[ch, rgb_ch]

        # Layer 2: 2D pair interactions
        pairs = [
            (0, 1, _PAIR_RG),  # R, G
            (0, 2, _PAIR_RB),  # R, B
            (1, 2, _PAIR_GB),  # G, B
        ]
        for ch_a, ch_b, coeffs in pairs:
            a = grid[:, ch_a] / (255.0 / (_PAIR_DIM - 1))
            b = grid[:, ch_b] / (255.0 / (_PAIR_DIM - 1))
            a_lo = np.clip(np.floor(a).astype(np.int32), 0, _PAIR_DIM - 2)
            b_lo = np.clip(np.floor(b).astype(np.int32), 0, _PAIR_DIM - 2)
            a_f = a - a_lo
            b_f = b - b_lo
            # Bilinear interpolation
            c = coeffs[ch]
            value += (
                c[a_lo, b_lo] * (1 - a_f) * (1 - b_f)
                + c[a_lo + 1, b_lo] * a_f * (1 - b_f)
                + c[a_lo, b_lo + 1] * (1 - a_f) * b_f
                + c[a_lo + 1, b_lo + 1] * a_f * b_f
            )

        # Layer 3: 3D trilinear correction
        coords = []
        for rgb_ch in range(3):
            v = grid[:, rgb_ch] / (255.0 / (_TRI_DIM - 1))
            lo = np.clip(np.floor(v).astype(np.int32), 0, _TRI_DIM - 2)
            frac = v - lo
            coords.append((lo, frac))

        tri = _TRILINEAR[ch]
        for dr in range(2):
            for dg in range(2):
                for db in range(2):
                    w = (
                        ((1 - coords[0][1]) if dr == 0 else coords[0][1])
                        * ((1 - coords[1][1]) if dg == 0 else coords[1][1])
                        * ((1 - coords[2][1]) if db == 0 else coords[2][1])
                    )
                    ri = coords[0][0] + dr
                    gi = coords[1][0] + dg
                    bi = coords[2][0] + db
                    value += w * tri[ri, gi, bi]

        result[:, ch] = value

    lut = np.clip(np.round(result), 0, 255).astype(np.int32)

    # Layer 4: Apply sparse corrections for entries with large fitting error
    for idx, (dc, dm, dy, dk) in _SPARSE_CORRECTIONS.items():
        lut[idx] += [dc, dm, dy, dk]

    return np.clip(lut, 0, 255)


# ---------------------------------------------------------------------------
# Interpolation table generation (analytical tetrahedral weights)
# ---------------------------------------------------------------------------
def generate_interp_tables() -> npt.NDArray[np.uint8]:
    """Generate tetrahedral interpolation weight tables analytically.

    The tables encode tetrahedral interpolation weights for 3D LUT lookup.
    For each combination of (r_frac, g_frac, b_frac) in 0..16, the table
    stores: [total_weight, w0, w1, w2, w3, w4, w5, w6, w7] where w0..w7
    are weights for the 8 cube corners.

    Corner assignment (flat LUT offsets [0, 17, 1, 18, 289, 306, 290, 307]):
      0: (r_lo, g_lo, b_lo)      4: (r_hi, g_lo, b_lo)
      1: (r_lo, g_hi, b_lo)      5: (r_hi, g_hi, b_lo)
      2: (r_lo, g_lo, b_hi)      6: (r_hi, g_lo, b_hi)
      3: (r_lo, g_hi, b_hi)      7: (r_hi, g_hi, b_hi)

    Returns:
        (17, 289, 9) uint8 array matching interp_tables.bin format.
    """
    w_lut = _INTERP_WEIGHT
    tables = np.zeros((17, 17 * 17, 9), dtype=np.uint8)

    for b_f in range(17):
        for g_f in range(17):
            for r_f in range(17):
                row = g_f * 17 + r_f

                # Sort fractional values to determine tetrahedron
                # Each axis maps to a corner offset bit:
                #   r -> bit 2 (corner 4), g -> bit 0 (corner 1), b -> bit 1 (corner 2)
                fracs = [(r_f, 4), (g_f, 1), (b_f, 2)]
                fracs_sorted = sorted(fracs, key=lambda x: x[0], reverse=True)

                f1_val, f1_axis = fracs_sorted[0]
                f2_val, f2_axis = fracs_sorted[1]
                f3_val, _f3_axis = fracs_sorted[2]

                # Barycentric coordinates as differences
                d0 = 16 - f1_val
                d1 = f1_val - f2_val
                d2 = f2_val - f3_val
                d3 = f3_val

                # Assign weights to corners via cumulative axis bits
                w = [0] * 8
                w[0] = w_lut[d0]
                w[f1_axis] = w_lut[d1]
                w[f1_axis | f2_axis] = w_lut[d2]
                w[7] = w_lut[d3]  # All axes = corner 7

                total = sum(w)
                tables[b_f, row, 0] = total
                for c in range(8):
                    tables[b_f, row, c + 1] = w[c]

    return tables


