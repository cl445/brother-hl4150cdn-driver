def generate_gamma_curve(curve_id: int) -> npt.NDArray[np.uint8]:
    """Generate a 256-byte gamma LUT.

    Args:
        curve_id: 0 or 1, selects the gamma curve.

    Returns:
        np.ndarray shape (256,) dtype uint8.

    Raises:
        ValueError: If `curve_id` is not 0 or 1.
    """
    if curve_id == 0:
        return np.frombuffer(_GAMMA_0, dtype=np.uint8).copy()
    if curve_id == 1:
        return np.frombuffer(_GAMMA_1, dtype=np.uint8).copy()
    msg = f"Invalid gamma curve_id {curve_id}, must be 0 or 1"
    raise ValueError(msg)


